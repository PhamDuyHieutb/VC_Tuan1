package Bai4;

public class Dictance {

	public int Dic(int x,int y){
		return (int) Math.sqrt((x-800)*(x-800)+(y-800)*(y-800));
	}
	
	public int Dic2(int x,int y){
		return (int) Math.sqrt((x-4000)*(x-4000)+(y-800)*(y-800));
	}
	
	public int Dic3(int x,int y){
		return (int) Math.sqrt((x-2400)*(x-2400)+(y-2400)*(y-2400));
	}
	
	
}
